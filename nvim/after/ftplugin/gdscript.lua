-- nvim/after/ftplugin/gdscript.lua

-- Set indentation rules for GDScript
vim.opt_local.tabstop = 4        -- Tabs are 4 spaces wide
vim.opt_local.shiftwidth = 4     -- Each indent level is 4 spaces
vim.opt_local.softtabstop = 4    -- A tab inserts 4 spaces
vim.opt_local.expandtab = false  -- Use actual tabs, not spaces

